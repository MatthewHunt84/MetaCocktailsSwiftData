import Foundation

enum Flavor: String, Codable, CaseIterable {
    case lemon          = "Lemon"
    case lime           = "Lime"
    case cream          = "Heavy Cream"
    case orange         = "Orange"
    case ginger         = "Ginger"
    case honey          = "Honey"
    case passionfruit   = "Passionfruit"
    case cucumber       = "Cucumber"
    case almond         = "Almond"
}

